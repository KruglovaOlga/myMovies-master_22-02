    private void tableCenter(){
        DefaultTableCellRenderer cellR = new DefaultTableCellRenderer();
        cellR.setHorizontalAlignment(SwingConstants.CENTER);
        cellR.setHorizontalTextPosition(SwingConstants.CENTER);
        // για τις στήλες μες αριθμούς
        jTable1.getColumnModel().getColumn(0).setCellRenderer(cellR); 
        jTable1.getColumnModel().getColumn(2).setCellRenderer(cellR);
        // για strings
        jTable1.setDefaultRenderer(String.class, cellR);
    }//end tableCenter
    
